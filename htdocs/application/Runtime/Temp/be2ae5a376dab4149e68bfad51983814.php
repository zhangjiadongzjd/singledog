<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; 妙聚网络发行总监：宋军</span>
	</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; 宋总您好，关于《锦衣夜行》这款游戏，可以和大家简单介绍一下吗？</span>
</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; 宋军：《锦衣夜行》是一款历史题材的ARPG武侠网页游戏，运用最先进的flsah技术和成熟的底层架构，构筑出了一个人物刻画精细，技能表现绚丽，背景原画精致的武侠世界。玩家将进入多方势力角逐的明朝初年，经历了从朱元璋裁撤锦衣卫，到朱允文继位削藩，最后导致靖难之变，朱棣成功登基一系列事件。在历史的剧烈变革中，展现出一个个有血有肉、有情有义的锦衣卫成员所经历的故事。</span>
	</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; 那么《锦衣夜行》中有哪些特色玩法推荐给大家呢？</span>
</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; 宋军：《锦衣夜行》游戏本身以爽快的战斗为主，结合了角色收集、红颜培养、酷炫坐骑、绝世神兵、修炼武功秘籍等武侠风味浓厚的特色玩法。研发团队精益求精，对角色展现采用了3D建模渲染技术，游戏中的人物、坐骑、红颜都力求精致，活灵活现，希望带给玩家最好的视觉体验。</span>
	</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; 《锦衣夜行》是一款由电视剧改编的ARPG游戏，为什么会选择这个IP？</span>
</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; 宋军：架空历史小说《锦衣夜行》作者月关是起点平台白金作家，自2011年在起点中文网连载，总点击过两千万，出版荣获金石堂畅销榜首。同名电视剧《锦衣夜行》系钟澍佳等执导，由张翰、朴敏英、徐正曦主演，乐视网在线播放。《锦衣夜行》这个IP拥有广大的读者群体和演员粉丝团，改编游戏具备良好的群众基础。</span>
	</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; IP的影响力对游戏起到的作用大吗？</span>
</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp;&nbsp;宋军：一款优秀的IP作品，具备了受众广、转化高、架构完整、协同推广等多个有点。《锦衣夜行》拥有大量潜在优质用户，可从IP知名度受益；同时故事内容丰富，脉络清晰，有助于游戏研发；另外，可进行多渠道联合推广，与制作方、网播方相互配合，打组合拳。</span>
	</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; 您对于IP泛娱乐化是怎么看的？</span>
</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; 宋军：优质IP作用是巨大的，它是带动粉丝经济、提高投资回报的有力武器。很多企业也迷信“得IP得天下”，但事实上，“囤IP”并不少见，拥有了它并不意味就有高利润回报，如何“运营IP”、物尽其用才是真正的大命题。当今IP泛娱乐从分散走向集中，融合影视、游戏、硬件、电商等多领域，线上有机融合线下，跨界建立生态圈。另外，提升原创性、避免过度消费，弱品牌化、强生命化，也是我们要去做的。</span>
	</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; 《锦衣夜行》已经上线，您对《锦衣夜行》目前的成绩满意吗？</span>
</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; 宋军：《锦衣夜行》从11月上线了20家一线页游平台，稳扎稳打，目前日流水在35万左右，进入12月以来，伴随开服数和平台支持力度的增大，流水、DAU还在稳步增长。我们对产品和市场都是有信心的，相信可以做得更好。</span>
	</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; 目前同类型的产品非常多，《锦衣夜行》是如何完成突围的？</span>
</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; 宋军：优秀的测试期数据、强力的IP号召力、高度的支撑配合，以及主动营销、主动服务</span>
	</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> 此次在《锦衣夜行》推广上，会有哪些新渠道和方式？</span>
</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp;&nbsp;宋军：我们与星座达人“同道大叔”达成了深入合作，微信、微博都有内容定制，而且他在跟我们合作之后荣升亿万富豪，侧面证明我们招财实力强劲。</span>
	</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; 对于《锦衣夜行》未来的发展有什么期待？</span>
</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; 宋军：影视剧播映将带起一波小高潮，接下来还会采用异业合作，拓宽品牌影响力。</span>
	</p>
<p style="font-family:" font-size:16px;text-indent:2em;background-color:#ffffff;"=""><span style="font-size:14px;"> &nbsp; 对一直关注《锦衣夜行》的玩家，有什么想说的？</span>
